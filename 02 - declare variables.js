/* Data types:
- Undefined
- Null
- Boolean
- String
- Symbol
- Number
- Object
*/

// String - Any form of text
// Number - A number
// Undefined - Something that hasn't been defined
// Null - nothing
// Boolean - true or false value
// Symbol - Immutable primitive value that is unique
// Object - Something that can store key-value pairs.

//------------------------------------------------------//

// One way to declare a variable is using the var keyword:
var myName = "Charlie"
myName = 8
myName = "Charles"

// Another way is using let:
let myAge = 17

// And another way is using const
const pi = 3.14159

/* Here are the differences: 
  - Var: can be used throughout the whole program
  - Let: only used within the scope of where it's declared 
  - Const: a variable that can't and shouldn't ever be changed
*/
